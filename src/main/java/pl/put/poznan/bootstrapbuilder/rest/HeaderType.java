package pl.put.poznan.bootstrapbuilder.rest;

/**
 * Header presence and type
 */
public enum HeaderType {
    /**
     * No header element
     */
    NONE,

    /**
     * Header element with style 'position: static'
     */
    STATIC,

    /**
     * Header element with style 'position: fixed'
     */
    FIXED
}
